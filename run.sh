#!/bin/bash

# Функция запуска проекта
start_app() {
    echo "======================================================"
    echo "🚀 Запуск Target Portfolio..."
    echo "======================================================"

    if [ ! -f .env ]; then
        cp .env.example .env
        echo "[i] Создан базовый файл настроек .env"
    fi

    echo "[i] Поднимаем базу данных и приложение (подождите)..."
    docker compose up -d --build
}

# Перехватываем Ctrl+C, чтобы скрипт возвращался в меню
trap 'echo -e "\n[i] Прерывание... Возврат в меню."; sleep 1; show_menu' SIGINT

show_menu() {
    clear
    echo "======================================================"
    echo "✅ Программа успешно запущена!"
    echo "🌐 Откройте сайт в браузере: http://localhost:8000"
    echo "🌐 Документация доступна на: http://localhost:8000/docs"
    echo "======================================================"
    echo ""
    echo "Выберите действие:"
    echo "[1] - Смотреть логи (нажмите Ctrl+C для возврата сюда)"
    echo "[2] - Полный сброс (удалить базу данных и перезапустить)"
    echo "[3] - Выключить программу и выйти"
    echo ""

    read -n 1 -p "Ваш выбор (1, 2 или 3): " choice
    echo ""

    if [ "$choice" == "1" ]; then
        echo ""
        echo "[i] Включен просмотр логов..."
        echo "[!] Нажмите Ctrl+C для возврата в меню"
        echo "------------------------------------------------------"
        docker compose logs -f
        show_menu
    elif [ "$choice" == "2" ]; then
        echo ""
        echo "======================================================"
        echo "⚠️  ОПАСНОСТЬ: ВЫ УВЕРЕНЫ?"
        echo "Это действие безвозвратно удалит все данные портфеля!"
        echo "======================================================"
        read -n 1 -p "Нажмите 'y' для сброса или любую другую клавишу для отмены: " confirm
        echo ""
        if [[ "$confirm" == "y" || "$confirm" == "Y" ]]; then
            echo ""
            echo "[i] Удаление данных и остановка контейнеров..."
            docker compose down -v --remove-orphans
            echo "[i] Данные очищены. Запускаем проект заново..."
            sleep 2
            start_app
            show_menu
        else
            echo "[i] Сброс отменен."
            sleep 1
            show_menu
        fi
    elif [ "$choice" == "3" ]; then
        echo ""
        echo "[i] Остановка всех сервисов..."
        docker compose down
        echo ""
        echo "✅ Программа полностью выключена."
        exit 0
    else
        show_menu
    fi
}

# Сначала запускаем приложение
start_app
# Затем показываем интерактивное меню
show_menu
